在PHP中将数据分为三大类八小类：
简单（基本）数据类型：4个小类
整型：int/integer，系统分配4个字节存储，表示整数类型（有前提）浮点型：float/double，系统分配8个字节存储，表示小数或者整型存不下的整数字符串型：string，系统根据实际长度分配，表示字符串（引号）布尔类型：
boo/boolean，表示布尔类型，只有两个值：true和false复合数据类型：2个小类
对象类型：object，存放对象（面向对象）数组类型：array，存储多个数据（一次性）特殊数据类型：2个小类
资源类型：resource，存放资源数据（PHP外部数据，如数据库、文件）
空类型：NULL，只有一个值就是NULL（不能运算）