# 最简单的函数----------------------------------------------------------------
# 传递参数 并在内部调用该参数,也可以不传参数
def fun(x):
    print(x)


fun(100)  # 100


# 不定长参数----------------------------------------------------------------
# 使用*args 或 **kwargs, 可以接纳更多参数范围
def fun2(x, y=2, *args, **kwargs):
    print(x, y, args, kwargs)


fun2(100, 200, 300, 400)     # 100 200 | (300, 400)  | {}
fun2(100, 200, 300, a=1, b=2)  # 100 200 | (300,)      | {'a': 1, 'b': 2}
fun2(100, 200, a=1, b=2)     # 100 200 | ()          | {'a': 1, 'b': 2}
